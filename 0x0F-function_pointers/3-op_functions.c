#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - adds a and b
 * @a: int one
 * @b: int two
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts a nd b
 * @a: int one
 * @b: int two
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a and b
 * @a: int one
 * @b: int two
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a and b
 * @a: int one
 * @b: int two
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus of a and b
 * @a: int one
 * @b: int two
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
